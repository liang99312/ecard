    private void setupEvents() {
        btnOk.addActionListener(new ActionListener() {

            @Override
            public void actionPerformed(ActionEvent e) {
                String old_pass = String.valueOf(jPasswordField1.getPassword());
                String new_pass = String.valueOf(jPasswordField2.getPassword());
                String new_repeat_pass = String.valueOf(jPasswordField3.getPassword());
                if (CommUtil.exists("from SysParameter s where s.sysParameter_key='cryptA01PassWord'")) {
                    old_pass = CipherUtil.encodeByMD5(old_pass);
                    new_pass = CipherUtil.encodeByMD5(new_pass);
                    new_repeat_pass = CipherUtil.encodeByMD5(new_repeat_pass);
                }
                if (!old_pass.equals(UserContext.password)) {
                    JOptionPane.showMessageDialog(JOptionPane.getFrameForComponent(btnOk), "原密码错误", "错误", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                if (!new_pass.equals(new_repeat_pass)) {
                    JOptionPane.showMessageDialog(JOptionPane.getFrameForComponent(btnOk), "两次密码不一致", "错误", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                ValidateSQLResult result = null;
                if (!new_pass.equals(old_pass)) {
                    String sql;
                    if (UserContext.isSA) {
                        sql = "update SysParameter set sysparameter_value='" + new_pass + "' where sysParameter_key='SysManPass'";
                    } else {
                        sql = "update a01password set pswd='" + new_pass + "' where a01_key='" + UserContext.person_key + "'";
                    }
                    result = CommUtil.excuteSQL(sql);
                }
                if (result.getResult() == 0) {
                    log.info("$原密码:" + old_pass + ",修改为:" + new_pass);
                    UserContext.password = new_pass;
                    JOptionPane.showMessageDialog(JOptionPane.getFrameForComponent(btnOk), "修改成功", "提示", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    MsgUtil.showHRSaveErrorMsg(result.getMsg());
                }
                dispose();
            }
        });
        CloseAction.doCloseAction(btnCancel);
    }
